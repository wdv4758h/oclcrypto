inline uchar4 AES_MixColumn(uchar4 state)
{
    return (uchar4)(
        AES_Galois2[state.s0] ^ AES_Galois3[state.s1] ^ state.s2  ^ state.s3,
        state.s0 ^ AES_Galois2[state.s1] ^ AES_Galois3[state.s2] ^ state.s3,
        state.s0 ^ state.s1 ^ AES_Galois2[state.s2] ^ AES_Galois3[state.s3],
        AES_Galois3[state.s0] ^ state.s1 ^ state.s2 ^ AES_Galois2[state.s3]
    );
}

inline uchar16 AES_MixColumns(uchar16 state)
{
    return (uchar16)(
        AES_MixColumn(state.s0123),
        AES_MixColumn(state.s4567),
        AES_MixColumn(state.s89AB),
        AES_MixColumn(state.sCDEF)
    );
}

inline uchar4 AES_InverseMixColumn(uchar4 state)
{
    return (uchar4)(
        AES_Galois14[state.s0] ^ AES_Galois11[state.s1] ^ AES_Galois13[state.s2] ^ AES_Galois9[state.s3],
        AES_Galois9[state.s0]  ^ AES_Galois14[state.s1] ^ AES_Galois11[state.s2] ^ AES_Galois13[state.s3],
        AES_Galois13[state.s0] ^ AES_Galois9[state.s1]  ^ AES_Galois14[state.s2] ^ AES_Galois11[state.s3],
        AES_Galois11[state.s0] ^ AES_Galois13[state.s1] ^ AES_Galois9[state.s2]  ^ AES_Galois14[state.s3]
    );
}

inline uchar16 AES_InverseMixColumns(uchar16 state)
{
    return (uchar16)(
        AES_InverseMixColumn(state.s0123),
        AES_InverseMixColumn(state.s4567),
        AES_InverseMixColumn(state.s89AB),
        AES_InverseMixColumn(state.sCDEF)
    );
}

