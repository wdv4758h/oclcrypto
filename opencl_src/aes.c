//      | 2 3 1 1 |    // 1 means no change
// M =  | 1 2 3 1 |    // 2 means left shift
//      | 1 1 2 3 |    // 3 means left shift + XOR
//      | 3 1 1 2 |
//
// To speed this part up we use lookup tables.

